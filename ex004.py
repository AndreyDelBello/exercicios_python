msg = input('Digite algo: ')

print(msg)
print('O tipo primitivo desse valor é:', type(msg))
print('Tem espaços?', msg.isspace())
print('É um valor numerico?', msg.isnumeric())
print('É um valor alfabetico?', msg.isalpha())
print('É alphanumerico?', msg.isalnum())
print('Está em maiusculas?', msg.isupper())
print('Está em minusculas?', msg.islower())
print('Está capitalizada?', msg.istitle())







